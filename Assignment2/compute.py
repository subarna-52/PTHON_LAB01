import math

power = 5 ** 8

sqrt_value = math.sqrt(400)

exponent_value = math.exp(5)

log_value = math.log(625, 5)

print(f"a) 5 to the power of 8: {power}")
print(f"b) Square root of 400: {sqrt_value}")
print(f"c) Exponent of 5 (e^5): {exponent_value}")
print(f"d) Logarithm of 625 base 5: {log_value}")
