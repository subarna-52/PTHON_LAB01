import math

sin_60 = math.sin(math.radians(60))

cos_pi = math.cos(math.pi)

sin_value = math.sin(0.8660254037844386)

tan_90 = math.tan(math.radians(90))

print(f"a) sin of 60 degrees: {sin_60}")
print(f"b) cos of pi: {cos_pi}")
print(f"c) sin(0.8660254037844386): {sin_value}")
print(f"d) tan of 90 degrees: {tan_90}")
